package ru.itmo.soalab2.model;

public class CoordinatesFromClient {
    private int id;
    private String x;
    private String y;


    public CoordinatesFromClient(int id, String x, String y) {
        this.id = id;
        this.x = x;
        this.y = y;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getX() {
        return x;
    }

    public void setX(String x) {
        this.x = x;
    }

    public String getY() {
        return y;
    }

    public void setY(String y) {
        this.y = y;
    }
}
